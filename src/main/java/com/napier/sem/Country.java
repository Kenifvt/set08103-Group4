package com.napier.sem;

public class Country {
    //Initialising variables for Country
    public String code;
    public String name;
    public String region;
    public String continent;
    public double surfaceArea;
    public int IndepYear;
    public int population;
    public double lifeExpectancy;
    public double GNP;
    public double GNPOLD;
    public String localName;
    public String governmentFrom;
    public String headOfState;
    public int capital;
    public String code2;

    //Get and Set methods for each Country variable.
    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }



    public String getContinent() {
        return continent;
    }

    public void setContinent(String continent) {
        this.continent = continent;
    }


    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }



    public double getSurfaceArea() {
        return surfaceArea;
    }

    public void setSurfaceArea(double surfaceArea) {
        this.surfaceArea = surfaceArea;
    }


    public int getIndepYear() {
        return IndepYear;
    }

    public void setIndepYear(int indepYear) {
        IndepYear = indepYear;
    }


    public int getPopulation() {
        return population;
    }

    public void setPopulation(int population) {
        this.population = population;
    }


    public double getLifeExpectancy() {
        return lifeExpectancy;
    }

    public void setLifeExpectancy(double lifeExpectancy) {
        this.lifeExpectancy = lifeExpectancy;
    }


    public double getGNP() {
        return GNP;
    }

    public void setGNP(double GNP) {
        this.GNP = GNP;
    }


    public double getGNPOLD() {
        return GNPOLD;
    }

    public void setGNPOLD(double GNPOLD) {
        this.GNPOLD = GNPOLD;
    }


    public void setLocalName(String localName) {
        this.localName = localName;
    }

    public void setGovernmentFrom(String governmentFrom) {
        this.governmentFrom = governmentFrom;
    }

    public void setHeadOfState(String headOfState) {
        this.headOfState = headOfState;
    }

    public void setCapital(int capital) {
        this.capital = capital;
    }

    public void setCode2(String code2) {
        this.code2 = code2;
    }


}
